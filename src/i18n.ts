const messages = { ko, en };

export function createTranslator(locale: Locale) {
  return (key: MessageKey, values: Record<string, string | number> = {}) =>
    messages[locale][key].replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`));
}

export function initialLocale(storage: Pick<Storage, 'getItem'> | null = typeof localStorage === 'undefined' ? null : localStorage, browserLanguage = typeof navigator === 'undefined' ? 'ko' : navigator.language): Locale {
  const saved = storage?.getItem('dgx-dashboard-locale');
  if (saved === 'ko' || saved === 'en') return saved;
  return browserLanguage.toLowerCase().startsWith('ko') ? 'ko' : 'en';
}

export function localizeAvailabilityReason(reason: string | undefined, locale: Locale) {
  const t = createTranslator(locale);
  return reason === '미설치' ? t('notInstalled') : t('lookupUnavailable');
}

export function localizeCollectorError(error: string, locale: Locale) {
  if (locale === 'ko') return error;
  const labels: Record<string, string> = { 저장소: 'Storage', 프로세스: 'Processes', 네트워크: 'Network' };
  const separator = error.indexOf(':');
  if (separator < 0) return error;
  const label = error.slice(0, separator);
  return `${labels[label] ?? label}${error.slice(separator)}`;
}
